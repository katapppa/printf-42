#include "ft_printf.h"

void    ft_color(int a)
{
    if(a==1)
        write(1, "\x1b[31m", 5);
    if(a==2)
        write(1, "\x1b[32m", 5);
    if(a==3)
        write(1, "\x1b[33m", 5);
    if(a==4)
        write(1, "\x1b[34m", 5);
    if(a==5)
        write(1, "\x1b[35m", 5);
    if(a==6)
        write(1, "\x1b[36m", 5);
    if(a==0)
        write(1, "\x1b[0m",4);
}
